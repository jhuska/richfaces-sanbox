package org.richfaces.demo.common;

import java.util.ArrayList;
import java.util.List;

import javax.annotation.PostConstruct;
import javax.faces.bean.ManagedBean;
import javax.faces.bean.RequestScoped;

@ManagedBean
@RequestScoped
public class RichBean {
    
    private String host;
    private int port;
    private List<String> suggestions;
    private String docServer;
    
    @PostConstruct
    public void initialiseSuggestions() { 
        setSuggestions(new ArrayList<String>());
        getSuggestions().add("Foo");
        getSuggestions().add("Bar");
        getSuggestions().add("Blah");
        getSuggestions().add("Yeah");
        getSuggestions().add("Underground");
        getSuggestions().add("Still");
    }
    
    public void actionListener() {
        System.out.println("Action Listener called on the server!");
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public String getDocServer() {
        return docServer;
    }

    public void setDocServer(String docValue) {
        this.docServer = docValue;
    }

    public List<String> getSuggestions() {
        return suggestions;
    }

    public void setSuggestions(List<String> suggestions) {
        this.suggestions = suggestions;
    }
}